    public void escribirUsuarioGuardado() {
        FileOutputStream fw = null;
        ObjectOutputStream bw = null;
        try {
            fw = new FileOutputStream(binarioGuardarUsuario);
            bw = new ObjectOutputStream(fw);
            for (Usuarios t : listaUsuariosGuardados) {
                bw.writeObject(t);
            }
            bw.flush();
        } catch (IOException ex) {
        } finally {
            try {
                bw.close();
                fw.close();
            } catch (Exception ex) {
            }
        }
    }

    public void cargarUsuarioGuardado() {
        try {
            listaUsuariosGuardados = new ArrayList();
            Usuarios temp;
            if (binarioGuardarUsuario.exists()) {
                FileInputStream entrada
                        = new FileInputStream(binarioGuardarUsuario);
                ObjectInputStream objeto
                        = new ObjectInputStream(entrada);
                try {
                    while ((temp = (Usuarios) objeto.readObject()) != null) {
                        listaUsuariosGuardados.add(temp);
                    }
                } catch (EOFException e) {
                    //encontro el final del archivo
                }
                objeto.close();
                entrada.close();
            } //fin if           
        } catch (Exception ex) {
        }
    }

